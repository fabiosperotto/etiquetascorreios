    /**
     * Abrir tela de cadastro de Pessoas, deve remover outras telas internas
     * abertas no JDestopPane.Possui Thread para exibir tela de loading (gif animado)
     *
     * @param evt evento de clique principal do mouse
     */
    private void jMenuItemPessoasActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jMenuItemPessoasActionPerformed
        this.jDesktop.removeAll();
        SwingUtilities.invokeLater(() -> { //invocacao segura do jframe
            TelaLoading loading = new TelaLoading(this.traducoes);
            loading.setLocationRelativeTo(null);
            loading.setVisible(true);
            new Thread(() -> {
                InternalPessoas internaPessoas = new InternalPessoas(this.traducoes);
                this.jDesktop.add(internaPessoas);
                internaPessoas.carregarDados();//realizar o carregamento pesado enquanto loading ativo
                
                SwingUtilities.invokeLater(() -> {
                    //quando o jframe final carregar a tela de loading deve desaparecer:
                    internaPessoas.setVisible(true);
                    loading.setVisible(false);
                });
            }).start();
        });

    }//GEN-LAST:event_jMenuItemPessoasActionPerformed

