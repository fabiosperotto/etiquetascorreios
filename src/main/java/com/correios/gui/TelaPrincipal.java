    /**
     * Realiza a checagem de conectividade com banco de dados. Caso nao exista a
     * conexao, vai avisar ao usuario encerrando a aplicacao posteriormente
     */
    private void verificarConexaoBD() {
        SessionFactory sessionFactory = HibernateUtil.getSessionFactory();
        if (sessionFactory == null) {
            JOptionPane.showMessageDialog(this,
                    this.traducoes.getString("erro_conexao_desc"),
                    this.traducoes.getString("erro_conexao_titulo"),
                    JOptionPane.ERROR_MESSAGE);
            System.exit(0);
        }
    }

